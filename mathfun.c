#include<stdio.h>
#include<math.h>
int main()
{
    printf("Ceil\t: %f\n",ceil(3.2));
    printf("Ceil\t: %f\n",ceil(5.6));
    printf("Floor\t: %f\n",floor(3.3));
    printf("Floor\t: %f\n",floor(5.6));
    printf("sqrt\t: %f\n",sqrt(16));
    printf("sqrt\t: %f\n",sqrt(7));
    printf("pow\t: %f\n",pow(2,4));
    printf("pow\t: %f\n",pow(3,3));
    printf("abs\t: %d\n",abs(-15));
    printf("abs\t: %d",abs(-45));
    return 0;
}